// Switch függvény!

const switchFunction = (num) => {
    switch (num) {
      case 1: console.log('Egy');
        break;
      case 2: console.log('Kettő');
        break;
      default: console.log('Én csak kettőig tudok számolni');
    }
  }
  ;
  switchFunction(2);
  
